package operator;

public class Ex3_5 {

	public static void main(String[] args) {
		
		double d = 85.4;
		int score = (int)d;
		// 형변환이란 변수 또는 상수의 타입을 다른 타입으로 변환하는 것
		// 형변환은 다른 말로 캐스팅이라고도 하며 형변환을 실행하면 지정된 타입으로 변환 후에
		// 그 결과 값을 반환한다 피연산자의 값은 아무런 변화가 없다
		
		System.out.println("score = " + score);
		System.out.println("d =" + d); 
	}
}
